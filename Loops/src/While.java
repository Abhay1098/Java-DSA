public class While {
    public static void main(String[] args) {
        /*
            Syntax of WHILE Loop
                while(CONDITION)
                {
                    //body
                }
         */
        //Q- Print number from 1 to 10

        int num=0;
        while(num<=10)
        {
            System.out.println(num);
            num++;
        }
    }
}
